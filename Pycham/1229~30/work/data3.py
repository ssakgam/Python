# 숫자 자료형
print(100)
print(3.14)
# type() 자료형을 반환 *****
print(type(100)) # int - 정수
print(type(3.14)) # float - 실수


# 사칙 연산자.
# 덧셈 , 뺄셈, 곱셈, 나눗셈
# +, -, *, /
# print("", 숫자)
print("5 + 7 =", 5 + 7)
print("5 + 7 = " + str(5+7))
# 여러분이 해봅니다.

