# 정수 나누기 연산자 : // => 나눈 다음에 소수점 이하 자릿 수 삭제한 후 정수만 남김
print(" 3 / 2 = ", 3 / 2)
print(" 3 // 2 = ", 3 // 2)

# % 나머지 연산자.
# - A를 B로 나누었을 때 나머지를 구함
